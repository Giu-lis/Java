public class Convenio {
    String nome;
    String plano;
    Double tarifa;

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public String getPlano() {
        return plano;
    }
    public void setPlano (String plano) {
        this.plano = plano;
    }
    public Double getTarifa () {
        return tarifa;
    }
    public void setTarifa (Double tarifa) {
        this.tarifa = tarifa;
    }
}
